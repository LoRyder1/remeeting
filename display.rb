When humans talk, we tend to utter "disfluencies" that interrupt the flow of speech, e.g. 'um', 'uh', 'ah', '[cough]', etc...
Your task is to create a simple app that takes strings as inputs, which can include any subset of the noises mentioned above, and then stores and displays the strings stripped of all the noises.
This is the Display challenge, so stay focused on design and styling of the end result. The goal is to make a a collection of disfluent strings into something human readable.
